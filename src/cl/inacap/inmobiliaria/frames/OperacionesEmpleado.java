    public OperacionesEmpleado(boolean operacion, int idEmpleado)
    {
        empleados = new EmpleadoDAO();
        this.operacion = operacion;
        if (idEmpleado != 0)
            this.empleado = empleados.getEmpleado(idEmpleado);
        initComponents();
        
        //si esto es a√±adir un nuevo empleado
        
        if (!operacion)
        {
            clearFields();
        }
        //si esto es modificar un empleado
        if (operacion)
        {
            setFields(empleado);
        }
    }
    
    private DefaultComboBoxModel setTipoEmpleadoModel()
    {
        String[] list = empleados.getListaTipoEmpleado();
        
        DefaultComboBoxModel empModel = new DefaultComboBoxModel();
        
        for (int i = 0; i < list.length; i++)
            empModel.addElement(list[i]);
        
        return empModel;
    }

    
    private void clearFields()
    {
        operacionLbl.setText("Operacion actual: Agregar nuevo empleado");
        idField.setText("" + empleados.getEmpleadoAutoincrement());
        nombreField.setText("");
        apellidoPatField.setText("");
        apellidoMatField.setText("");
        runField.setText("");
        tipoEmpleadoComboBox.setSelectedIndex(0);
        identificadorField.setText("");
        fotoField.setText("");
    }
    
    private void setFields(Empleado emp)
    {
        operacionLbl.setText("Operacion actual: Modificar usuario existente");
        idField.setText("" + emp.getIdEmpleado());
        nombreField.setText(emp.getNombre());
        apellidoPatField.setText(emp.getApellidoPat());
        apellidoMatField.setText(emp.getApellidoMat());
        runField.setText(""+ emp.getRun());
        tipoEmpleadoComboBox.setSelectedIndex(emp.getTipoEmpleado()-1); //WARNING, CHECK MATCHES
        identificadorField.setText("" + emp.getIdentificador());
        fotoField.setText("" + emp.getFoto());
    }  
    
