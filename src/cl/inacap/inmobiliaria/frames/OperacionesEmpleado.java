    private void aceptarBtnActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_aceptarBtnActionPerformed
    {//GEN-HEADEREND:event_aceptarBtnActionPerformed
        if (!operacion)
        {
            empleados.addEmpleado(new Empleado(
                    0, nombreField.getText(), apellidoPatField.getText(), apellidoMatField.getText(), runField.getText(),
                    Integer.parseInt(identificadorField.getText()), tipoEmpleadoComboBox.getSelectedIndex() + 1, fotoField.getText()
            ));
        }
        /*
        public Empleado(int idEmpleado, String nombre, String apellidoPat, String apellidoMat, String run,
            int identificador, int tipoEmpleado, String foto)
    
        */
        if (operacion)
        {
            empleados.updateEmpleado(new Empleado(
                    Integer.parseInt(idField.getText()), nombreField.getText(), apellidoPatField.getText(), apellidoMatField.getText(), runField.getText(),
                    Integer.parseInt(identificadorField.getText()), tipoEmpleadoComboBox.getSelectedIndex() + 1, fotoField.getText()));
        }
        
        this.dispose();
    }//GEN-LAST:event_aceptarBtnActionPerformed

