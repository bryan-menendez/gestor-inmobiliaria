    private void setFields(Cliente cl)
    {
        operacionLbl.setText("Operacion actual: Modificar usuario existente");
        idField.setText("" + cl.getIdCliente());
        vendedorRadioButton.setSelected(cl.isComprador());
        nombreField.setText(cl.getNombre());
        apellidoPatField.setText(cl.getApellidoPat());
        apellidoMatField.setText(cl.getApellidoMat());
        runField.setText(""+ cl.getRun());
        comunaField.setText(cl.getComuna());
        diaField.setText(""+ cl.getFechaNac().substring(8, 10));
        mesField.setText(""+ cl.getFechaNac().substring(5, 7));
        a単oField.setText(""+ cl.getFechaNac().substring(0, 4));
        sexoComboBox.setSelectedIndex(cl.getSexo());
        ocupacionComboBox.setSelectedIndex(cl.getOcupacion());
        nivelEscolarComboBox.setSelectedIndex(cl.getNivelEscolar());
        estadoCivilComboBox.setSelectedIndex(cl.getEstadoCivil());
        viviendaPropiaCheckBox.setSelected(cl.getViviendaPropia());
        creditoBancarioField.setText("" + cl.getCreditoBancario());
        subsidioField.setText("" + cl.getSubsidio());
        rentaField.setText("" + cl.getRentaBruta());
        porcRegSocField.setText("" + cl.getPorcFichaReg());
        
        if (cl.isComprador())
        {
            compradorRadioButton.setSelected(true);
            switchPanelComprador(true);
        }
        else
        {
            vendedorRadioButton.setSelected(true);
            switchPanelComprador(false);
        }
    }  
       
