    private void aceptarBtnActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_aceptarBtnActionPerformed
    {//GEN-HEADEREND:event_aceptarBtnActionPerformed
        
        if (!operacion)
        {
            cuentas.addCuenta(usernameField.getText(), passwordField.getText(), Integer.parseInt(idEmpField.getText()));
        }
        if (operacion)
        {
            cuentas.updateCuenta(Integer.parseInt(idField.getText()), usernameField.getText(), passwordField.getText(), 
                                Integer.parseInt(idEmpField.getText()));
        }
        
        this.dispose();
    }//GEN-LAST:event_aceptarBtnActionPerformed

