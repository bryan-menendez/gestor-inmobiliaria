    private DefaultComboBoxModel setTipoEmpleadoModel()
    {
        String[] list = cuentas.getListaTipoEmpleado();
        
        DefaultComboBoxModel tipoModel = new DefaultComboBoxModel();
        
        for (int i = 0; i < list.length; i++)
            tipoModel.addElement(list[i]);
        
        return tipoModel;
    }
    
    private void clearFields()
    {
        operacionLbl.setText("Operacion actual: Agregar nueva cuenta");
        idField.setText("" + cuentas.getCuentaAutoincrement());
        
        usernameField.setText("");
        passwordField.setText("");
        idEmpField.setText("");
    }
    
    private void setFields(Cuenta acc)
    {
        operacionLbl.setText("Operacion actual: Modificar usuario existente");
        idField.setText("" + acc.getIdCuenta());
        
        usernameField.setText(acc.getUsername());
        passwordField.setText("");
        idEmpField.setText("" + acc.getIdEmpleado());
    }  
       
