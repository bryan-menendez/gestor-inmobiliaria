    private void aceptarBtnActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_aceptarBtnActionPerformed
    {//GEN-HEADEREND:event_aceptarBtnActionPerformed
        if (!operacion)
        {
            propiedades.addPropiedad(new Propiedad(0, "", !terrenoRadioButton.isSelected(), Integer.parseInt(tamañoField.getText()), Integer.parseInt(cantConstField.getText()), calleField.getText(),
                    poblacionField.getText(), sectorComboBox.getSelectedIndex(), comunaField.getText(), Integer.parseInt(pisosField.getText()),
                    Integer.parseInt(bañosField.getText()), Integer.parseInt(dormitoriosField.getText()), patioDelCheckBox.isSelected(), patioTrasCheckBox.isSelected(),
                    estacionamientoCheckBox.isSelected(), Integer.parseInt(tasacionField.getText()), comentarioField.getText()
            ));
            
            /*
           public Propiedad(int idPropiedad, String fechaPublicacion, boolean tipoPropiedad, int tamaño, int cantidadConstruido, String calle, 
            String poblacion, int sector, String comuna, int pisos, int baños, int dormitorios, boolean patioDel, boolean patioTras, 
            boolean estacionamiento, int tasacion,  String comentario)
    
            */
        }
        if (operacion)
        {
            propiedades.updatePropiedad(new Propiedad(Integer.parseInt(idField.getText()), "", !terrenoRadioButton.isSelected(), Integer.parseInt(tamañoField.getText()), Integer.parseInt(cantConstField.getText()), calleField.getText(),
                    poblacionField.getText(), sectorComboBox.getSelectedIndex(), comunaField.getText(), Integer.parseInt(pisosField.getText()),
                    Integer.parseInt(bañosField.getText()), Integer.parseInt(dormitoriosField.getText()), patioDelCheckBox.isSelected(), patioTrasCheckBox.isSelected(),
                    estacionamientoCheckBox.isSelected(), Integer.parseInt(tasacionField.getText()), comentarioField.getText()
            ));
        }
        
        this.dispose();
        /*
       public Propiedad(int idPropiedad, String fechaPublicacion, boolean tipoPropiedad, int tamaño, int cantidadConstruido, String calle, 
            String poblacion, int sector, String comuna, int pisos, int baños, int dormitorios, boolean patioDel, boolean patioTras, 
            boolean estacionamiento, int tasacion,  String comentario)
    */
    }//GEN-LAST:event_aceptarBtnActionPerformed

