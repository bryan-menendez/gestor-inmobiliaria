    private DefaultComboBoxModel setSectorModel()
    {
        String[] list = {"Norte", "Sur", "Centro", "Este", "Oeste"};
        
        DefaultComboBoxModel sexoModel = new DefaultComboBoxModel();
        
        for (int i = 0; i < list.length; i++)
            sexoModel.addElement(list[i]);
        
        return sexoModel;
    }
    
    private void switchPanelInmueble(boolean state)
    {
        jPanel2.setEnabled(state);
        patioTrasCheckBox.setEnabled(state);
        patioDelCheckBox.setEnabled(state);
        estacionamientoCheckBox.setEnabled(state);
        pisosLbl.setEnabled(state);
        pisosField.setEnabled(state);
        bañosLbl.setEnabled(state);
        bañosField.setEnabled(state);
        dormLbl.setEnabled(state);
        dormitoriosField.setEnabled(state);

        if(!state)
        {
            pisosField.setText("0");
            bañosField.setText("0");
            dormitoriosField.setText("0");
        }
    }
    
    private void clearFields()
    {
        operacionLbl.setText("Operacion actual: Agregar nueva propiedad");
        idField.setText("" + propiedades.getAutoincrement());
        terrenoRadioButton.setSelected(true);
        tamañoField.setText("");
        cantConstField.setText("");
        calleField.setText("");
        poblacionField.setText("");
        comunaField.setText("");
        sectorComboBox.setSelectedIndex(0);
        patioTrasCheckBox.setSelected(false);        
        patioDelCheckBox.setSelected(false);       
        estacionamientoCheckBox.setSelected(false);
        pisosField.setText("");
        bañosField.setText("");
        dormitoriosField.setText("");
        tasacionField.setText("");
        comentarioField.setText("");
        switchPanelInmueble(false);
    }
    
