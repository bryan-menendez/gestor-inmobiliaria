    {
        operacionLbl.setText("Operacion actual: Modificar propiedad existente");
        idField.setText("" + prop.getIdPropiedad());
        tamañoField.setText("" + prop.getTamaño());
        cantConstField.setText("" + prop.getCantidadConstruido());
        calleField.setText("" + prop.getCalle());
        poblacionField.setText("" + prop.getPoblacion());
        comunaField.setText("" + prop.getComuna());
        sectorComboBox.setSelectedIndex(prop.getSector());
        patioTrasCheckBox.setSelected(prop.isPatioTras());        
        patioDelCheckBox.setSelected(prop.isPatioDel());       
        estacionamientoCheckBox.setSelected(prop.isEstacionamiento());
        pisosField.setText("" + prop.getPisos());
        bañosField.setText("" + prop.getBaños());
        dormitoriosField.setText("" + prop.getDormitorios());
        tasacionField.setText("" + prop.getTasacion());
        comentarioField.setText("" + prop.getComentario());
        
        if (!prop.getTipoPropiedad())
        {
            terrenoRadioButton.setSelected(true);
            switchPanelInmueble(false);
        }
        else
        {
            inmuebleRadioButton.setSelected(true);
            switchPanelInmueble(true);
        }
    }  
       
